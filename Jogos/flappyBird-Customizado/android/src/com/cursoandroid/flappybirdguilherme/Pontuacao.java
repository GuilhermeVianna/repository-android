public class Pontuacao {
    private String pontos;
    private String melhorPontuacao;
    private String piorPontuacao;

    public Pontuacao() {
    }

    public String getPontos() {
        return pontos;
    }

    public void setPontos(String pontos) {
        this.pontos = pontos;
    }

    public String getMelhorPontuacao() {
        return melhorPontuacao;
    }

    public void setMelhorPontuacao(String melhorPontuacao) {
        this.melhorPontuacao = melhorPontuacao;
    }

    public String getPiorPontuacao() {
        return piorPontuacao;
    }

    public void setPiorPontuacao(String piorPontuacao) {
        this.piorPontuacao = piorPontuacao;
    }

}
